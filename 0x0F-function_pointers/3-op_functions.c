/**
 * op_add - add a and b
 *
 * @a: int
 * @b: int
 * Return: int
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - substract a from b
 *
 * @a: int
 * @b: int
 * Return: int
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiply a and b
 *
 * @a: int
 * @b: int
 * Return: int
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divide a per b
 *
 * @a: int
 * @b: int
 * Return: int
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - remainder or div a per b
 *
 * @a: int
 * @b: int
 * Return: int
 */
int op_mod(int a, int b)
{
	return (a % b);
}
