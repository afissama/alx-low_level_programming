#include <stdio.h>
#include <stdlib.h>
#include "main.h"

/**
 * print_diagonal - print diagonal
 * @n: The number as distance
 *
 */

void print_diagonal(int n)
{
	int i;
	int j;

	if (n > 0)
	{
		for (i = 0; i < n; i++)
		{
			for (j = 0; j < i; j++)
			{
				_putchar(' ');
			}
			_putchar('\\');
			_putchar('\n');
		}
	}
	_putchar('\n');
}
